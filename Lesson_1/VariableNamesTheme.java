public class VariableNamesTheme {
    public static void main(String[] args) {
        System.out.println("\n1. Разные переменные:");

        // Цифра
        int number = 7;
        System.out.println("Цифра = " + number);

        // Сумма чисел
        int sumDigits = 15;
        System.out.println("Сумма чисел = " + sumDigits);

        // Произведение цифр
        int productOfDigits = 56;
        System.out.println("Произведение цифр = " + productOfDigits);

        // Максимальное число
        int maxNumber = 100;
        System.out.println("Максимальное число = " + maxNumber);

        // Количество десятков
        int tensCount = 3;
        System.out.println("Количество десятков = " + tensCount);

        // Вес собаки
        int dogWeight = 13;
        System.out.println("Вес собаки = " + dogWeight);

        // Исходное число
        int initialNumber = 42;
        System.out.println("Исходное число = " + initialNumber);

        // Процент по вкладу
        int interestRate = 5;
        System.out.println("Процент по вкладу = " + interestRate);

        // Переменная хранит символ &
        char ampersandChar = '&';
        System.out.println("Переменная хранит символ = " + ampersandChar);

        // Код ошибки
        int errorCode = 404;
        System.out.println("Код ошибки = " + errorCode);

        // Тип сообщения
        String messageType = "Ошибка";
        System.out.println("Тип сообщения = " + messageType);

        // Число нулей
        int zeroCount = 2;
        System.out.println("Число нулей = " + zeroCount);

        // Уникальное число
        int uniqueNumber = 987654321;
        System.out.println("Уникальное число = " + uniqueNumber);

        // Случайное число
        int randomNumber = 100;
        System.out.println("Случайное число = " + randomNumber);

        // Математическое выражение
        String mathExpression = "2 + 2 * 2";
        System.out.println("Математическое выражение = " + mathExpression);

        // Счет в игре
        int gameScore = 75;
        System.out.println("Счет в игре = " + gameScore);

        // Максимальная длина
        int maxLength = 255;
        System.out.println("Максимальная длина = " + maxLength);

        // Пункт меню
        String menuItem = "Главная";
        System.out.println("Пункт меню = " + menuItem);

        // Стоимость кофе
        int coffeePrice = 3;
        System.out.println("Стоимость кофе = " + coffeePrice);

        // Дата начала
        String startDate = "2024-01-01";
        System.out.println("Дата начала = " + startDate);

        // Окончание диапазона
        int endRange = 100;
        System.out.println("Окончание диапазона = " + endRange);

        // Полное имя работника месяца
        String employeeOfMonth = "Иван Иванов";
        System.out.println("Полное имя работника месяца = " + employeeOfMonth);

        // Заголовок электронной книги
        String ebookTitle = "Война и мир";
        System.out.println("Заголовок электронной книги = " + ebookTitle);

        // Размер
        int size = 42;
        System.out.println("Размер = " + size);

        // Вместимость
        int capacity = 100;
        System.out.println("Вместимость = " + capacity);

        // Счетчик
        int counter = 10;
        System.out.println("Счетчик = " + counter);

        // Путь до файла
        String filePath = "/file.txt";
        System.out.println("Путь до файла = " + filePath);

        // Количество чисел в каждой строке
        int numbersPerLine = 5;
        System.out.println("Количество чисел в каждой строке = " + numbersPerLine);

        System.out.println("\n2. Boolean-переменные:");

        // Сотни равны?
        boolean isEqualHundreds = true;
        System.out.println("Сотни равны? = " + isEqualHundreds);

        // Компьютер включен?
        boolean isComputerOn = false;
        System.out.println("Компьютер включен? = " + isComputerOn);

        // Есть равные цифры?
        boolean hasEqualDigits = false;
        System.out.println("Есть равные цифры? = " + hasEqualDigits);

        // Создано?
        boolean isCreated = true;
        System.out.println("Создано? = " + isCreated);

        // Пустое?
        boolean isEmpty = false;
        System.out.println("Пустое? = " + isEmpty);

        // Активное?
        boolean isActive = true;
        System.out.println("Активное? = " + isActive);

        // Новое?
        boolean isNew = false;
        System.out.println("Новое? = " + isNew);

        // Электронная почта действительная?
        boolean isValidEmail = true;
        System.out.println("Электронная почта действительная? = " + isValidEmail);

        // Имеются уникальные строки?
        boolean hasUniqueStrings = true;
        System.out.println("Имеются уникальные строки? = " + hasUniqueStrings);

        System.out.println("\n3. Аббревиатуры:");

        // Старый universally unique identifier
        String oldUuid = "123e4567-e89b-12d3-a456-426614174000";
        System.out.println("Старый universally unique identifier = " + oldUuid);

        // Производитель оперативной памяти
        String ramManufacturer = "Kingston";
        System.out.println("Производитель оперативной памяти = " + ramManufacturer);

        // Емкость жесткого диска
        int hddCapacity = 1024;
        System.out.println("Емкость жесткого диска = " + hddCapacity);

        // Протокол передачи гипертекста
        String httpProtocol = "HTTP/1.1";
        System.out.println("Протокол передачи гипертекста = " + httpProtocol);

        // Сокращенный uniform resource locator
        String shortUrl = "https://example.com/test";
        System.out.println("Сокращенный uniform resource locator = " + shortUrl);

        // Новый идентификатор клиента
        String newCid = "CLIENT123456";
        System.out.println("Новый идентификатор клиента = " + newCid);

        // American standard code for information interchange
        String asciiCode = "ASCII";
        System.out.println("American standard code for information interchange = " + asciiCode);
    }
}